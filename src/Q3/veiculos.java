package Q3;

public  class veiculos {

		String plaque;
		int year;
		
		public veiculos(String plaque, int year) {
			super();
			this.plaque = plaque;
			this.year = year;
		}
		

		public String getPlaque() {
			return plaque;
		}

		public void setPlaque(String plaque) {
			this.plaque = plaque;
		}

		public int getYear() {
			return year;
		}

		public void setYear(int year) {
			this.year = year;
		}
		
			
		
	}


